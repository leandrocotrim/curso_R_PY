# logical
# TRUE, FALSE
v = TRUE
print(v)
print(class(v))


# numeric
# 1.2, 3, 897
v = 2
print(v)
print(class(v))


# integer
# 1L, 3L, 
v = 99L
print(v)
print(class(v))


# complex
# 2i, 4i
v = 2 + 3i
print(v)
print(class(v))

# character
# '1', "A", 'Leandro', "TRUE", '12,9'
v = "FALSE"
print(v)
print(class(v))


# raw
# 48 65 6c 6c 6f
v = charToRaw("Hello")
print(v)
print(class(v))










